# pide al usuario que ingrese un numero e indica si es un numero
# positivo, negativo o cero

Numero_1=int(input("Ingresa un Numero  "))

if Numero_1>0:
        print(f"El numero ingresado {Numero_1} es positivo")
elif Numero_1<0:
        print(f"El numero ingresado{Numero_1} es negativo")
else:
        print("El numero ingresado es 0")
